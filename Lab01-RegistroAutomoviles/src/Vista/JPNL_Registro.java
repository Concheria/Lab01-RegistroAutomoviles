    public void clearFields()
    {
        jtf_Numero.setText("");
        jtf_Nombre.setText("");
        jtf_Cedula.setText("");
        jtf_Placa.setText("");
    }
    
    public void estadoSinAgregar()
    {
        jtf_Numero.setEnabled(false);
        jb_Buscar.setEnabled(false);
    }
    
    public void estadoAgregar()
    {
        jtf_Numero.setEnabled(true);
        jb_Buscar.setEnabled(true);
    }
    
