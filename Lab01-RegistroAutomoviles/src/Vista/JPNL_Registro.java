    public String[] getTodo()
    {
        String[] todo = new String[4];
        
        todo[0] =  jtf_Numero.getText();
        todo[1] = jtf_Nombre.getText();
        todo[2] = jtf_Cedula.getText();
        todo[3] = jtf_Placa.getText();
                    
        return todo;
    }

